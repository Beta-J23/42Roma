void	ft_rev_int_tab(int *tab, int size)
{
	int	temp;
	int	j;

	j = 0;
	while (j < (size / 2))
	{
		temp = tab[j];
		tab[j] = tab[size - 1 - j];
		tab[size - 1 - j] = temp;
		j++;
	}
}
