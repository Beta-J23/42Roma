#include <unistd.h>

void	print(char c)
{
	write(1, &c, 1);
}

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	i = 0;
	j = 1;
	while (j <= argc)
	{
		while (argv[j][i] != '\0')
		{
			i++;
		}
		j++;
	}
}
